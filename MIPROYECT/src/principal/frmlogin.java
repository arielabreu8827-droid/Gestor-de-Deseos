    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                
    // Obtener los valores que el usuario escribió
    String usuario = txtusuario.getText();
    String password = new String(txtpassword.getPassword());

    // Validar credenciales
    if(usuario.equals("ADMIN") && password.equals("12345")){
        // Si las credenciales son correctas, abrir la otra ventana
        FrmDeseos ventana = new FrmDeseos();
        ventana.setVisible(true);
        ventana.setLocationRelativeTo(null);
        this.dispose();
    } else {
        // Si son incorrectas, mostrar mensaje
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Usuario o contraseña incorrectos", 
            "Error de acceso", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }


    }//GEN-LAST:event_jButton1ActionPerformed

